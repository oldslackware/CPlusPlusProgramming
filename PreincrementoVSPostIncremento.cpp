#include<iostream>
using std::cout;
using std::endl;
int main(){
    int x=5;
    cout<<"PostIncremento"<<endl;
    cout<<x<<endl;
    cout<<"x++ "<<x++<<endl;
    cout<<"x aggiornata "<<x<<endl;
    
    cout<<"Preincremento"<<endl;
    cout<<x<<endl;
    cout<<"++x "<<++x<<endl;
    cout<<"x aggiornata "<<x<<endl;
    system("PAUSE");
    return 0;
}
